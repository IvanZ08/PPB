package com.example.projectfirebase;

public class Pegawai {
    private String nomor_peg;
    private String nama_peg;
    private String jataban;

    public String getNomor_peg() {
        return nomor_peg;
    }

    public void setNomor_peg(String nomor_peg) {
        this.nomor_peg = nomor_peg;
    }

    public String getNama_peg() {
        return nama_peg;
    }

    public void setNama_peg(String nama_peg) {
        this.nama_peg = nama_peg;
    }

    public String getJataban() {
        return jataban;
    }

    public void setJataban(String jataban) {
        this.jataban = jataban;
    }
}
